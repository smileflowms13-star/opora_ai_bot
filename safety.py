from __future__ import annotations

import re
from dataclasses import dataclass


@dataclass(frozen=True)
class RiskResult:
    is_high: bool
    category: str = "none"
    matched: str = ""


def normalize_text(text: str) -> str:
    """
    Нормализация текста для safety-фильтра.

    Это не диагностика и не медицинская оценка.
    Это технический фильтр безопасности, который должен не пропускать
    потенциально кризисные сообщения в обычный AI-диалог.
    """
    if not text:
        return ""

    text = str(text).lower()
    text = text.replace("ё", "е")

    # Частые варианты маскировки.
    replacements = {
        "0": "о",
        "3": "з",
        "4": "ч",
        "@": "а",
        "$": "с",
    }

    for old, new in replacements.items():
        text = text.replace(old, new)

    # Убираем лишнюю пунктуацию, но оставляем буквы/цифры/пробелы/дефисы.
    text = re.sub(r"[^\w\s\-]+", " ", text, flags=re.UNICODE)
    text = re.sub(r"\s+", " ", text).strip()

    return text


