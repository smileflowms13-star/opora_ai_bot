def normalize_text(text: str) -> str:
    """
    袧芯褉屑邪谢懈蟹邪褑懈褟 褌械泻褋褌邪 写谢褟 safety-褎懈谢褜褌褉邪.

    协褌芯 薪械 写懈邪谐薪芯褋褌懈泻邪 懈 薪械 屑械写懈褑懈薪褋泻邪褟 芯褑械薪泻邪.
    协褌芯 褌械褏薪懈褔械褋泻懈泄 褎懈谢褜褌褉 斜械蟹芯锌邪褋薪芯褋褌懈, 泻芯褌芯褉褘泄 写芯谢卸械薪 薪械 锌褉芯锌褍褋泻邪褌褜
    锌芯褌械薪褑懈邪谢褜薪芯 泻褉懈蟹懈褋薪褘械 褋芯芯斜褖械薪懈褟 胁 芯斜褘褔薪褘泄 AI-写懈邪谢芯谐.
    """
    if not text:
        return ""

    text = str(text).lower()
    text = text.replace("褢", "械")

    # 效邪褋褌褘械 胁邪褉懈邪薪褌褘 屑邪褋泻懈褉芯胁泻懈.
    replacements = {
        "0": "芯",
        "3": "蟹",
        "4": "褔",
        "@": "邪",
        "$": "褋",
    }

    for old, new in replacements.items():
        text = text.replace(old, new)

    # 校斜懈褉邪械屑 谢懈褕薪褞褞 锌褍薪泻褌褍邪褑懈褞, 薪芯 芯褋褌邪胁谢褟械屑 斜褍泻胁褘/褑懈褎褉褘/锌褉芯斜械谢褘/写械褎懈褋褘.
    text = re.sub(r"[^\w\s\-]+", " ", text, flags=re.UNICODE)
    text = re.sub(r"\s+", " ", text).strip()

    return text


